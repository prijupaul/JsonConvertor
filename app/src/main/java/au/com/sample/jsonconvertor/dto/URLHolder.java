public class URLHolder {
    private String url;
    private String title;

    /**
     * return url
     * @return
     */
    public String getUrl() {
        return url;
    }

    /**
     * set url
     * @param url
     */
    public void setUrl(String url) {
        this.url = url;
    }

    /**
     * return title
     * @return
     */
    public String getTitle() {
        return title;
    }

    /**
     * set Title
     * @param title
     */
    public void setTitle(String title) {
        this.title = title;
    }
}
